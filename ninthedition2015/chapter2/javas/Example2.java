package ninthedition2015.chapter2.classes;

class Example2{
	public static void main(String args[]){
		int num; // в этой строке объявляется переменная с именем num
		
		num = 100; // в этой строке num присваивается значение 100
		
		System.out.println(num);
		
		num = num * 2; // Значение num умножается на 2
		
		System.out.println("Значение переменной num * 2 равно ");
		System.out.println(num);
	}
}